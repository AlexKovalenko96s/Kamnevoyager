package ua.kas.main;

public abstract class Car {

	protected String id;
	protected String mark;
	protected String model;
	protected String type;
	protected String number;

	public String getId() {
		return id;
	}

	public String getMark() {
		return mark;
	}

	public String getModel() {
		return model;
	}

	public String getType() {
		return type;
	}

	public String getNumber() {
		return number;
	}
}
